import mongoose from 'mongoose';

const christmasProductSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  category: {
    type: String,
    required: true,
    enum: ['Charcuteries', 'Fromages', 'Boissons', 'Desserts']
  },
  price: {
    type: Number,
    required: true,
    min: 0
  },
  cost: {
    type: Number,
    required: true,
    min: 0
  },
  stock: {
    type: Number,
    required: true,
    min: 0,
    default: 0
  },
  description: {
    type: String,
    trim: true,
    default: ''
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const ChristmasProduct = mongoose.model('ChristmasProduct', christmasProductSchema);

export default ChristmasProduct;
